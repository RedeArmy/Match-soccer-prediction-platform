func TestSchemaHeader_ContainsRFC3339Timestamp(t *testing.T) {
	before := time.Now().UTC().Truncate(time.Second)
	h := schemaHeader()
	after := time.Now().UTC().Add(time.Second).Truncate(time.Second)

	for _, line := range strings.Split(h, "\n") {
		ts := strings.TrimPrefix(line, "-- Generated: ")
		if ts == line {
			continue // prefix not present on this line
		}
		parsed, err := time.Parse(time.RFC3339, ts)
		if err != nil {
			t.Errorf("expected RFC3339 timestamp after '-- Generated: ', got %q", ts)
			return
		}
		if parsed.Before(before) || parsed.After(after) {
			t.Errorf("timestamp %s is outside expected range [%s, %s]", parsed, before, after)
		}
		return
	}
	t.Error("schemaHeader does not contain a '-- Generated: ...' line")
}

// ── filterDumpLines ───────────────────────────────────────────────────────────

func TestFilterDumpLines_EmptyInput_ReturnsEmpty(t *testing.T) {
	if got := filterDumpLines(""); got != "" {
		t.Errorf("expected empty output for empty input, got %q", got)
	}
}

func TestFilterDumpLines_RemovesSetLines(t *testing.T) {
	raw := "SET statement_timeout = 0;\nCREATE TABLE foo (id int);\nSET lock_timeout = 0;"
	got := filterDumpLines(raw)
	if strings.Contains(got, "SET ") {
		t.Errorf("SET lines must be removed, got:\n%s", got)
	}
	if !strings.Contains(got, "CREATE TABLE") {
		t.Errorf("CREATE TABLE must be preserved, got:\n%s", got)
	}
}

func TestFilterDumpLines_RemovesIndentedSetLines(t *testing.T) {
	raw := "  SET search_path = public, pg_catalog;\nCREATE TABLE t (id int);"
	got := filterDumpLines(raw)
	if strings.Contains(got, "SET ") {
		t.Errorf("indented SET lines must be removed, got:\n%s", got)
	}
}

func TestFilterDumpLines_RemovesSelectPgCatalog(t *testing.T) {
	raw := "SELECT pg_catalog.set_config('search_path', '', false);\nCREATE TABLE bar (id int);"
	got := filterDumpLines(raw)
	if strings.Contains(got, "SELECT pg_catalog") {
		t.Errorf("SELECT pg_catalog lines must be removed, got:\n%s", got)
	}
	if !strings.Contains(got, "CREATE TABLE") {
		t.Errorf("CREATE TABLE must be preserved, got:\n%s", got)
	}
}

func TestFilterDumpLines_RemovesBackslashLines(t *testing.T) {
	raw := "\\connect quiniela_schema\nCREATE TABLE baz (id int);\n\\restrict\n\\unrestrict"
	got := filterDumpLines(raw)
	for _, line := range strings.Split(got, "\n") {
		if strings.HasPrefix(strings.TrimSpace(line), "\\") {
			t.Errorf("backslash line must be removed, found: %q", line)
		}
	}
	if !strings.Contains(got, "CREATE TABLE") {
		t.Errorf("CREATE TABLE must be preserved, got:\n%s", got)
	}
}

func TestFilterDumpLines_PreservesNormalDDL(t *testing.T) {
	raw := "CREATE TABLE users (id BIGSERIAL PRIMARY KEY);\nCREATE INDEX idx_users ON users(id);"
	got := filterDumpLines(raw)
	if got != raw {
		t.Errorf("normal DDL must be preserved unchanged\nwant:\n%s\ngot:\n%s", raw, got)
	}
}

func TestFilterDumpLines_MixedInput_FiltersCorrectly(t *testing.T) {
	raw := strings.Join([]string{
		"SET statement_timeout = 0;",
		"SELECT pg_catalog.set_config('search_path', '', false);",
		"\\connect mydb",
		"CREATE TABLE users (id BIGSERIAL PRIMARY KEY);",
		"SET lock_timeout = 0;",
		"CREATE INDEX idx ON users(id);",
		"\\restrict",
	}, "\n")

	got := filterDumpLines(raw)

	for _, bad := range []string{"SET ", "SELECT pg_catalog", "\\"} {
		for _, line := range strings.Split(got, "\n") {
			if strings.HasPrefix(strings.TrimSpace(line), bad) {
				t.Errorf("filtered output must not contain %q prefix, found line: %q", bad, line)
			}
		}
	}
	for _, want := range []string{"CREATE TABLE users", "CREATE INDEX idx"} {
		if !strings.Contains(got, want) {
			t.Errorf("filtered output must contain %q\ngot:\n%s", want, got)
		}
	}
}

// ── generate (integration — requires Docker) ──────────────────────────────────

func TestGenerate_WritesValidSchema(t *testing.T) {
	if testing.Short() {
		t.Skip("requires Docker (skipped with -short)")
	}
	ctx, cancel := context.WithTimeout(context.Background(), 3*time.Minute)
	defer cancel()

	outPath := filepath.Join(t.TempDir(), "schema.sql")
	if err := generate(ctx, outPath); err != nil {
		t.Fatalf("generate: %v", err)
	}

	data, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("read generated schema: %v", err)
	}
	content := string(data)

	if !strings.Contains(content, "AUTO-GENERATED") {
		t.Error("generated schema must contain AUTO-GENERATED header")
	}
	for _, line := range strings.Split(content, "\n") {
		trimmed := strings.TrimSpace(line)
		if strings.HasPrefix(trimmed, "\\") {
			t.Errorf("backslash meta-commands must be filtered from output, found: %q", line)
		}
	}
}

