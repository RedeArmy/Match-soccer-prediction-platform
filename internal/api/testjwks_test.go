// testJWKSServer creates an ephemeral RSA-2048 key pair, starts an httptest.Server
// that serves the public key as a JWKS document, and returns:
//   - the JWKS URL (to pass as WCQ_CLERK_JWKSURL to RequireAuth)
//   - a sign function that generates a signed JWT string for a given subject
//
// The httptest.Server is closed automatically when the test ends.
// Callers that need to set Authorization headers should prefix the token with "Bearer ".
//
// Implementation notes:
//   - A stable kid ("test-key") is set on the JWK so that the signed JWT header
//     carries the kid, enabling fast key lookup when RequireAuth calls jwt.Parse.
//   - Signing uses the JWK object (not the raw *rsa.PrivateKey) so the kid is
//     automatically embedded in the JWT header. Without this, the parser has no
//     kid to match against and may fail to select the correct key.
//   - Cache-Control: max-age=3600 prevents the lestrrat-go/jwx cache from
//     treating the cached key set as immediately stale.
func testJWKSServer(t *testing.T) (jwksURL string, signJWT func(subject string) string) {
	t.Helper()

	privateKey, err := rsa.GenerateKey(rand.Reader, 2048)
	if err != nil {
		t.Fatalf("testJWKSServer: generate RSA key: %v", err)
	}

	rawJWK, err := jwk.FromRaw(privateKey)
	if err != nil {
		t.Fatalf("testJWKSServer: build private JWK: %v", err)
	}
	if err := rawJWK.Set(jwk.AlgorithmKey, jwa.RS256); err != nil {
		t.Fatalf("testJWKSServer: set algorithm: %v", err)
	}
	if err := rawJWK.Set(jwk.KeyIDKey, "test-key"); err != nil {
		t.Fatalf("testJWKSServer: set key ID: %v", err)
	}

	pubJWK, err := rawJWK.PublicKey()
	if err != nil {
		t.Fatalf("testJWKSServer: derive public JWK: %v", err)
	}

	keySet := jwk.NewSet()
	if err := keySet.AddKey(pubJWK); err != nil {
		t.Fatalf("testJWKSServer: add key to set: %v", err)
	}

	keySetJSON, err := json.Marshal(keySet)
	if err != nil {
		t.Fatalf("testJWKSServer: marshal JWKS: %v", err)
	}

	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		w.Header().Set("Cache-Control", "max-age=3600")
		_, _ = w.Write(keySetJSON)
	}))
	t.Cleanup(srv.Close)

	// Sign using the JWK object so the kid is embedded in the JWT header.
	sign := func(subject string) string {
		tok, err := jwt.NewBuilder().
			Subject(subject).
			IssuedAt(time.Now()).
			Expiration(time.Now().Add(time.Hour)).
			Build()
		if err != nil {
			t.Fatalf("testJWKSServer: build JWT for %q: %v", subject, err)
		}
		signed, err := jwt.Sign(tok, jwt.WithKey(jwa.RS256, rawJWK))
		if err != nil {
			t.Fatalf("testJWKSServer: sign JWT for %q: %v", subject, err)
		}
		return string(signed)
	}

	return srv.URL, sign
}

// newAdminTestServerWithAuth builds a Server with a non-nil (but unreachable) database
// pool and a real JWKS endpoint so RequireAuth can validate tokens generated by signJWT.
// Use this in tests that need to exercise the middleware chain past the auth check.
// An unlimited LimiterStore is injected so that iterating all admin routes with the same
// user subject does not trigger 429 responses before the DB-unavailable path is reached.
func newAdminTestServerWithAuth(t *testing.T, jwksURL string) *api.Server {
	t.Helper()
	cfg := &config.Config{}
	cfg.Clerk.JWKSURL = jwksURL
	srv := api.New(fakePool(t), cfg, zaptest.NewLogger(t), messaging.NewInMemoryBus(nil), nil, nil)
	srv.SetLimiterStore(middleware.NewUnlimitedLimiterStore())
	return srv
}
